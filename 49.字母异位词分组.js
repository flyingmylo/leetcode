// @lc code=start
/**
 * @param {string[]} strs
 * @return {string[][]}
 */
var groupAnagrams = function (strs) {
	let map = new Map();
	for (const v of strs) {
		let codes = [...v].map((x) => x.charCodeAt()).sort().join();
		if (map.has(codes)) {
      map.get(codes).push(v);
		} else {
      map.set(codes, [v]);
		}
	}

	return [...map.values()]
};
